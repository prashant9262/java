
public class assignment_operator {
    public static void main(String[] args) {
        
        int p = 20;
        int q;

        // =      
        q = p;
        System.out.println(p);

        // += 
        p += q;
        System.out.println(p);

        // -= 
        p -= q;
        System.out.println(p);

        //*= 
        p *= q ;
        System.out.println(p);

        // /=
        p /= q;
        System.out.println(p);

        // %=
        p %= q;
        System.out.println(p);




    }
    
}
