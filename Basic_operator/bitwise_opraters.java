
public class bitwise_opraters {
    public static void main(String[] args) {
        
       int p = 10 , q = 20 ; 

       System.out.println(p | q );
       System.out.println(p & q );
       System.out.println(p ^ q );

       System.out.println(p << 1 );
       System.out.println(p >> 1 );

       System.out.println(q >> 2);
       System.out.println(q << 2);


    }
}
